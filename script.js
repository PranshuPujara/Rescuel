  function getLanguageItem(lang) {
    return languageItems.find((item) => item.code === lang) || languageItems[0];
  }

  function openLanguageMenu() {
    languageSwitcher.classList.add('is-open');
    languageTrigger.setAttribute('aria-expanded', 'true');
  }

  function closeLanguageMenu({ focusTrigger = false } = {}) {
    languageSwitcher.classList.remove('is-open');
    languageTrigger.setAttribute('aria-expanded', 'false');
    if (focusTrigger) languageTrigger.focus();
  }

  function syncLanguageUI(lang) {
    const activeLanguage = getLanguageItem(lang);
    const labelNode = document.querySelector('.lang-switcher-label');
    const labelText = labelNode ? labelNode.textContent.trim() : 'Language';

    languageSelected.textContent = activeLanguage.code === 'en'
      ? activeLanguage.native
      : `${activeLanguage.native} · ${activeLanguage.english}`;

    languageTrigger.setAttribute('aria-label', `${labelText}: ${activeLanguage.native}`);

    languageOptionButtons.forEach((button) => {
      const isSelected = button.dataset.lang === lang;
      button.classList.toggle('is-selected', isSelected);
      button.setAttribute('aria-selected', isSelected ? 'true' : 'false');
      button.tabIndex = isSelected ? 0 : -1;
    });
  }

  function focusLanguageOption(lang) {
    const targetButton = languageOptionButtons.find((button) => button.dataset.lang === lang) || languageOptionButtons[0];
    if (targetButton) targetButton.focus();
  }

  function setLanguage(lang) {
    applyTranslations(lang);
    syncLanguageUI(lang);
    closeLanguageMenu();
  }

  function applyTranslations(lang) {
    const dictionary = translations[lang] || translations.en;

    document.documentElement.lang = lang;

    document.querySelectorAll('[data-i18n]').forEach((node) => {
      const key = node.dataset.i18n;
      if (dictionary[key]) node.textContent = dictionary[key];
    });

    document.querySelectorAll('[data-i18n-html]').forEach((node) => {
      const key = node.dataset.i18nHtml;
      if (dictionary[key]) node.innerHTML = dictionary[key];
    });

    localStorage.setItem('rescuel-language', lang);
  }

