  languageMenu.addEventListener('keydown', (event) => {
    const activeButtons = languageOptionButtons;
    const currentIndex = activeButtons.indexOf(document.activeElement);

    if (event.key === 'Escape') {
      event.preventDefault();
      closeLanguageMenu({ focusTrigger: true });
      return;
    }

    if (event.key === 'ArrowDown') {
      event.preventDefault();
      const nextIndex = Math.min(activeButtons.length - 1, currentIndex + 1);
      activeButtons[nextIndex]?.focus();
      return;
    }

    if (event.key === 'ArrowUp') {
      event.preventDefault();
      const nextIndex = Math.max(0, currentIndex - 1);
      activeButtons[nextIndex]?.focus();
      return;
    }

    if (event.key === 'Home') {
      event.preventDefault();
      activeButtons[0]?.focus();
      return;
    }

    if (event.key === 'End') {
      event.preventDefault();
      activeButtons[activeButtons.length - 1]?.focus();
      return;
    }

    if (event.key === 'Enter' || event.key === ' ') {
      event.preventDefault();
      const option = document.activeElement.closest('[data-lang-option]');
      if (option) setLanguage(option.dataset.lang);
    }
  });

